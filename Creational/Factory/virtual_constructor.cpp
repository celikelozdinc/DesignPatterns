#include "virtual_constructor.h"

#include <memory>

int main() {
    std::cout << "============================================================================\n";
    C c1{};
    c1.whoami();
    std::cout << "============================================================================\n";
    auto c2 = std::make_unique<C>();
    c2->whoami();
    std::cout << "============================================================================\n";
    std::unique_ptr<A> ptrToB = std::make_unique<B>();
    ptrToB->whoami();
    std::cout << "============================================================================\n";
    std::unique_ptr<B> ptrToC = std::make_unique<C>();
    ptrToC->whoami();
    std::cout << "============================================================================\n";

}